    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String selectItemId = tm.getValueAt(tableItem.getSelectedRow(), 0).toString();
        try {
            if (ProductDAO.deleteProduct(selectItemId)) {
                JOptionPane.showMessageDialog(null, "Product Removed Successfully!", "Product Deleted", JOptionPane.INFORMATION_MESSAGE);
                btnDelete.setEnabled(true);

                tm.setRowCount(0);
                this.loadProducts();
                return;
            }
            JOptionPane.showMessageDialog(null, "Error! Product not Delete!", "Error", JOptionPane.INFORMATION_MESSAGE);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error while connecting to database", "Database Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

